public class forLoopOrnekleri3 {

	public static void main(String[] args) {
		// 2, 4, 8, 16, 32, 2 nin üsleri

		for (int i = 1 ; i < 100 ; i*=2 ) {
			
			System.out.println(i);
		}
				
	
	
	int us=1;
	for (int i = 0 ; i < 7 ; i++ ) {
		 
		System.out.print(" "+us);
		us=us*2;
	}	 
				
	}
	
	
}
	


